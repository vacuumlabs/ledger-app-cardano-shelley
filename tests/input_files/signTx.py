    name: str
    tx: Transaction
    signingMode: TransactionSigningMode
    txBody: str
    options: bool = False
    additionalWitnessPaths: List[str] = field(default_factory=list)
    expected_sw: Optional[Errors] = Errors.SW_SUCCESS
    # TODO: Debug navigation
    nano_skip: Optional[bool] = False


